# creating the list containing the desired object to sort 
numbers = [3, 1, 4, 1, 5, 9, 2]
letters = ['w', 'd', 'a', 'r']
words = ['Hello', 'Cat', 'Dog',]

# Apply the sorted() function for ordering the objects
sorted_numbers = sorted(numbers)
sorted_letters = sorted(letters)
sorted_words = sorted(words)

# print the results
print(sorted_numbers)
print(sorted_letters)
print(sorted_words)
